package com.qianfeng.PO;


public class TbDepreciation {

  private String depreciationId;
  private String stateId;
  private String depreciationPhoneId;
  private String depreciationAssessmentId;
  private long depreciationPrice;


  public String getDepreciationId() {
    return depreciationId;
  }

  public void setDepreciationId(String depreciationId) {
    this.depreciationId = depreciationId;
  }


  public String getStateId() {
    return stateId;
  }

  public void setStateId(String stateId) {
    this.stateId = stateId;
  }


  public String getDepreciationPhoneId() {
    return depreciationPhoneId;
  }

  public void setDepreciationPhoneId(String depreciationPhoneId) {
    this.depreciationPhoneId = depreciationPhoneId;
  }


  public String getDepreciationAssessmentId() {
    return depreciationAssessmentId;
  }

  public void setDepreciationAssessmentId(String depreciationAssessmentId) {
    this.depreciationAssessmentId = depreciationAssessmentId;
  }


  public long getDepreciationPrice() {
    return depreciationPrice;
  }

  public void setDepreciationPrice(long depreciationPrice) {
    this.depreciationPrice = depreciationPrice;
  }

}
